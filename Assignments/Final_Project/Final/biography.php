      <h2>Biography</h2>
      <p><h5>Most people have heard of Alan Turing. Every once in a while, people credit him with being the brain behind cracking the Enigma,
	  touted by Germans as being unbreakable, and until Turing, living up to the hype. He has also sometimes been called the father of theoretical
	  computer science and artificial intelligence("Alan Turing"). Turing was born in June of 1912 in London(Hodges, "/early"). In 1926 he was enrolled at Sherborne School, where he
	  received mostly negative reports from school, including science and mathematics(Hodges, "/early"). In 1935, he was elected a fellow at King's College(Hodges, "/spirit"). Also during his time
	  there, he proved the central limit theorem(Aldrich), and wrote a response to a problem known as the Entscheidungsproblem (Turing, "On Computable Numbers") (don't ask me to explain it).
	  In this, he introduced the theory of Turing machines(Turing, "On Computable Numbers"), which are machines that can solve any problem presented as an algorithm.</p>

	  <p>What Turing is most known for is breaking the Enigma code. This achievement (depending on the source) is credited with shortening the war 
	  from three to seven years. To do this, he created a machine called The Bombe("Bombe"). During this time he wrote two papers that British intelligence 
	  didn't release until 2012 (("The Applications"),("Paper on Statistics") [see release date]), as they were considered critical to British security. In 1946, he helped to create the first computer and software(Hodges, "/computer"),
	  and in the process, wrote and presented a paper on a stored-program computer, which is a computer that can electronically store program data. 
	  This was the first of what would eventually lead to modern computers. He was then hired as the Deputy Director of the Royal Society Computing Machine Laboratory (Hodges, "/computer"), 
	  where he wrote a paper on artificial intelligence, entitled "Computing Machinery and Intelligence."(Turing, "Computing Machinery") He then came up with the Turing test, in
	  an attempt to address whether something was a machine or had intelligence (Turing, "Computing Machinery"). This was the precursor to the modern CAPTCHA test ("CAPTCHA"). He also devised
	  software for a chess game during this time("Turochamp"). Shortly after that, he was arrested for gross indecency(Lee).In return for probation, he was sentenced 
	  to hormone therapy to reduce his libido. Two years after his conviction, he committed suicide by cyanide. However, this is being questioned now. (Pease)</p>

	  If you want more information on Alan Turing, a quick search on the internet will bring lots of information.
	  For an excellent movie based mostly on the cracking of the Enigma machine, I can personally recommend The Imitation Game</h5></p>
